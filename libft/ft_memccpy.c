#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	unsigned char *p;
	size_t	i;

	i = 0;
	while (i < n)
	{
		((unsigned char *)dst)[i] = ((unsigned char *)src)[i];
		if (((char *)dst)[i] == c)
		{
			p = &((unsigned char *)dst)[i + 1];
			return (p);
		}
		i++;
	}
	return (NULL);
}
