#include "libft.h"

int		ft_isprime(int n)
{
	int d;

	if (n <= 1)
		return (0);
	d = 2;
	while (d <= n / 2)
	{
		if (n % d == 0)
			return (0);
		d++;
	}
	return (1);
}
